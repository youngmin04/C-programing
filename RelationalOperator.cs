using System;
using System.Collections.Generic;
using System.Text;
using static System.Console;

namespace Programing
{
    class RelationalOperator
    {
        static void Main()
        {
            WriteLine($"3>4:{3>4}");
            WriteLine($"3>=4:{3 >= 4}");
            WriteLine($"3<4:{3 < 4}");
            WriteLine($"3<=4:{3 <= 4}");
            WriteLine($"3==4:{3 == 4}");
            WriteLine($"3!=4:{3 != 4}");
        }
    }
}
